/** Slot for prototype JS to swap radio/checkbox icons */
export function SelectionIcon({
  type,
  checked,
  className,
}: {
  type: "radio" | "checkbox";
  checked: boolean;
  className?: string;
}) {
  const color = checked ? "text-[#389656]" : type === "checkbox" ? "text-[#8d9191]" : "text-[#8d9191]";
  const cn = `${color} ${className ?? "size-6"}`.trim();
  if (type === "radio") {
    return checked ? (
      <IconRadioChecked className={cn} data-selection-icon="radio" />
    ) : (
      <IconRadioUnchecked className={cn} data-selection-icon="radio" />
    );
  }
  return checked ? (
    <IconCheckboxChecked className={cn} data-selection-icon="checkbox" />
  ) : (
    <IconCheckboxOutline className={cn} data-selection-icon="checkbox" />
  );
}
